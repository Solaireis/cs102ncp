
#include <cmath> // For math functions like sqrt() and pow()
#include <iostream>

using namespace std;

int main()
{
    // Using math functions
    double sqrt_res = sqrt(25);
    double pow_res = pow(2, 3);

    // Display results
    cout << "Square root of 25: " << sqrt_res << endl;
    cout << "2^3: " << pow_res << endl;

    return 0;
}